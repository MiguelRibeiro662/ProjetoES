public class Copia {
    private Integer idCopia;
    private java.lang.String cota;

    public Copia(Integer idCopia, String cota) {
        this.idCopia = idCopia;
        this.cota = cota;
    }

    public Integer getIdCopia() {
        return idCopia;
    }

    public String getCota() {
        return cota;
    }
}
